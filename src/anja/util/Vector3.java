package anja.util;

/**
* 3D-Vector
*
* @version      0.1 08.09.98
* @author       Thomas Kamphans
*/

public class Vector3
{
	//************************************************************
	// public variables
	//************************************************************

	public float x, y, z;

	//************************************************************
	// Constructors
	//************************************************************

	/**
	* create new vector, initialize elements with 0
	*/

	//============================================================
	public Vector3()
	//============================================================
	{
		x = 0;
		y = 0;
		z = 0;

	} // Vector3


	//============================================================
	public Vector3( float x_in, float y_in, float z_in )
	//============================================================
	{
		x = x_in;
		y = y_in;
		z = z_in;

	} // Vector3


	//************************************************************
	// Class Methods
	//************************************************************

	//************************************************************
	// Public Methods
	//************************************************************


	//============================================================
	public Object clone()
	//============================================================
	{

		return new Vector3( x, y, z );

	} // clone


	//============================================================
	public String toString()
	//============================================================
	{

		return "( " + x + ", " + y + ", " + z + " )";

	} // toString


	/**
	* Add Vector3
	*/

	//============================================================
	public void add ( Vector3 a )
	//============================================================
	{
		x += a.x;
		y += a.y;
		z += a.z;
	} // add


	/**
	* Calculate p' = A * p
	*/

	//============================================================
	public void transform ( Matrix33 a )
	//============================================================
	{
		float x_new = a.m[0][0] * x + a.m[0][1] * y + a.m[0][2] * z;
		float y_new = a.m[1][0] * x + a.m[1][1] * y + a.m[1][2] * z;
		float z_new = a.m[2][0] * x + a.m[2][1] * y + a.m[2][2] * z;

        x = x_new;
        y = y_new;
        z = z_new;
	} // transform


} // Vector3
