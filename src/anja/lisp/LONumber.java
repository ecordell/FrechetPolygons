package anja.lisp;

/**
* Container for Lisp numbers.
* 
* @author Tom Kamphans
*/

public class LONumber extends LispObj
{
   int _contents;

   //============================================================
   public LONumber()
   //============================================================
   {
   }

   //============================================================
   public LONumber( int n )
   //============================================================
   {
      _contents = n;
   }

   //============================================================
   public int getType()
   //============================================================
   {
 	return LispObj.NUMBER;
   }

   //============================================================
   public boolean isNumber()
   //============================================================
   {
 	return true;
   }

   //============================================================
   public String toString()
   //============================================================
   {
      return "Number: " + _contents;
   }


}
