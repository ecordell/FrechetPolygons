
package anja.lisp;


/**
* Container for Lisp dotted pairs.
* 
* @author Tom Kamphans
*/

public class LODPair extends LispObj
{
   LispObj _car = null;
   LispObj _cdr = null;


   //============================================================
   public LODPair()
   //============================================================
   {
   	_car = null;
	_cdr = null;	
   }

   //============================================================
   public LODPair( LispObj mycar,
                   LispObj mycdr )
   //============================================================
   {
	_car = mycar;
	_cdr = mycdr;
   }


   //============================================================
   public LispObj car()
   //============================================================
   {
	return _car;
   }

 
   //============================================================
   public LispObj cdr()
   //============================================================
   {
	return _cdr;
   }


   //============================================================
   public void setCar( LispObj newValue )
   //============================================================
   {
	_car = newValue; 
   }


   //============================================================
   public void setCdr( LispObj newValue )
   //============================================================
   {
	_cdr = newValue; 
   }


  //============================================================
   public int getType()
   //============================================================
   {
 	return LispObj.DPAIR;
   }

  //============================================================
   public boolean isDPair()
   //============================================================
   {
 	return true;
   }

}
