package anja.lisp;

/**
* Container for Lisp token. 
* 
* @author Tom Kamphans
*/

public class LispToken extends LispObj
{
   //************************************************************
   // private variables
   //************************************************************

   private int _tokenType;

   // ************************************************************************
   // Constructors
   // ************************************************************************

   //============================================================
   public LispToken()
   //============================================================
   {
      _tokenType = LispObj.NIL;      
   }

   //============================================================
   public LispToken( int type )
   //============================================================
   {
      _tokenType = type;
   }

   //************************************************************
   // class methods
   //************************************************************


   // ************************************************************************
   // Public methods
   // ************************************************************************


   //============================================================
   public void setType( int t )
   //============================================================
   {
      _tokenType = t;
   }

   //============================================================
   public int getType()
   //============================================================
   {
      return _tokenType;
   }


   //============================================================
   public String toString()
   //============================================================
   {
       switch ( _tokenType ) {
          case LispObj.OPENTOKEN: return "(TOKEN";
          case LispObj.CLOSETOKEN: return ")TOKEN";
          default: return "NULLTOKEN";
       }
   }

}
